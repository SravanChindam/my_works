#calculating area & perimeter of a square
size = int(input("enter a size for square : "))
#formula for calculating area of square
area_of_sqr = size * size
#formula for calculating perimeter of square
peri_of_sqr = 4 * size
print("area of a square is : ",area_of_sqr)
print("perimeter of a square is : ",peri_of_sqr)
